// Add loading state for better UX
window.addEventListener('load', () => {
    document.body.classList.add('loaded');
    
    // Animate stats numbers
    const stats = document.querySelectorAll('.stat h3');
    stats.forEach(stat => {
        const finalNumber = stat.textContent;
        if (finalNumber.includes('K') || finalNumber.includes('%')) {
            animateNumber(stat, finalNumber);
        }
    });
});

// Number animation function
function animateNumber(element, finalText) {
    const hasK = finalText.includes('K');
    const hasPercent = finalText.includes('%');
    const hasPlus = finalText.includes('+');
    
    let number = parseInt(finalText.replace(/[K%+]/g, ''));
    let current = 0;
    const increment = number / 50;
    
    const timer = setInterval(() => {
        current += increment;
        if (current >= number) {
            current = number;
            clearInterval(timer);
        }
        
        let displayText = Math.floor(current).toString();
        if (hasK) displayText += 'K';
        if (hasPercent) displayText += '%';
        if (hasPlus) displayText += '+';
        
        element.textContent = displayText;
    }, 50);
}